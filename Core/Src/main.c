/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  uint16_t adcVal = 0;
  uint32_t dacVal = 0;
  uint32_t loop_cnt = 0;

  unsigned int perf_cycles;

  float adcVolt     = 0.0f;
  float desiredVolt = 1.5f;
  float pidIn       = 0.0f;
  float pidOut      = 0.0f;

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_DAC1_Init();
  MX_ADC2_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  // TIM2 should have a period of 1us
  // use TIM2 to measure time spent in parts of software
  HAL_TIM_Base_Start(&htim2);

  // TIM1 should have a period of 1/8 kHz
  HAL_TIM_Base_Start(&htim1);
  HAL_TIM_OC_Start(&htim1, TIM_CHANNEL_1); // debug output

  HAL_ADC_Start(&hadc1);
  HAL_DAC_Start(&hdac1, DAC_CHANNEL_1);

  // initialize CMSIS PID controller
  arm_pid_instance_f32 myPID;
  myPID.Kp = 0.1f;
  myPID.Ki = 0.001f;
  myPID.Kd = 0.00f;

  arm_pid_init_f32(&myPID, 1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  // wait for TIM1 triggering ADC conversion at 8 kHz
	  HAL_ADC_PollForConversion(&hadc1, 1000);
	  // enter critical part: set GPIO to trigger oscilloscope
	  HAL_GPIO_WritePin(SAMPLE_OUT_GPIO_Port, SAMPLE_OUT_Pin, GPIO_PIN_SET);
	  // start performance counter (count CPU cycles)
	  perf_counter_reset();
	  perf_counter_start();

	  // read new ADC value
	  adcVal = (uint16_t)HAL_ADC_GetValue(&hadc1);

	  // convert ADC value to float voltage
	  adcVolt = (float)adcVal / 4096.0f * 3.3f;

	  // calculate delta ("error")
	  pidIn = desiredVolt - adcVolt;

	  // run PID with one sample
	  pidOut = arm_pid_f32(&myPID, pidIn);

	  // convert PID output to DAC value
	  dacVal = (uint32_t)( pidOut /3.3f * 4096.0f );
	  if (dacVal >= 4094)
		  dacVal = 4094;
	  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, dacVal);

	  // stop performance counter (at 287 cycles)
	  perf_counter_stop();
	  perf_cycles = perf_counter_value();
	  // exit critical part: reset GPIO
	  HAL_GPIO_WritePin(SAMPLE_OUT_GPIO_Port, SAMPLE_OUT_Pin, GPIO_PIN_RESET);

	  // use the remaining time to do other things
	  // (non-blocking !!!)
	  loop_cnt++;
	  if(loop_cnt > 8000){
		  loop_cnt = 0;
		  if(desiredVolt > 0.5f)
			  desiredVolt = 0.5f;
		  else
			  desiredVolt = 1.5f;
	  }
  }
  /* USER CODE END 3 */
}

